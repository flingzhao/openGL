        shader.Use();
        glm::mat4 model = glm::mat4(1.0f);
        glm::mat4 view = camera.GetViewMatrix();
        glm::mat4 projection = glm::perspective(glm::radians(45.f), (float)SCR_WIDTH / (float)SCR_HEIGHT, 0.1f, 100.0f);
        shader.SetMat4("model", model);
        shader.SetMat4("view", view);
        shader.SetMat4("projection", projection);
        shader.SetVec3("cameraPos", camera.GetCameraPosition());
        // // cubes
        glBindVertexArray(cubeVAO);
        glActiveTexture(GL_TEXTURE11);
        glBindTexture(GL_TEXTURE_2D, cubeTexture);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glBindVertexArray(0);

        modelShader.Use();
        // glActiveTexture(GL_TEXTURE5);
        // glBindTexture(GL_TEXTURE_CUBE_MAP, skyTexture);
        // modelShader.SetInt("skybox", 5);
        // std::cout << skyTexture << '\n';
        float scale = 0.1f;
        model = glm::mat4(scale);
        model = glm::translate(model, glm::vec3(0.0f, -2.0f, -1.0f)); // translate it down so it's at the center of the scene
        model = glm::scale(model, glm::vec3(0.15f, 0.15f, 0.15f));	// it's a bit too big for our scene, so scale it down
        modelShader.SetMat4("model", model);
        modelShader.SetMat4("view", view);
        modelShader.SetMat4("projection", projection);
        modelShader.SetVec3("cameraPos", camera.GetCameraPosition());
        nano.Draw(modelShader);


        // draw skybox as last
        glDepthFunc(GL_LEQUAL);  // change depth function so depth test passes when values are equal to depth buffer's content
        skyboxshader.Use();
        view = glm::mat4(glm::mat3(camera.GetViewMatrix())); // remove translation from the view matrix
        skyboxshader.SetMat4("view", view);
        skyboxshader.SetMat4("projection", projection);
        // skybox cube
        glBindVertexArray(skyVAO);
        glActiveTexture(GL_TEXTURE1);
        glBindTexture(GL_TEXTURE_CUBE_MAP, skyTexture);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glActiveTexture(GL_TEXTURE0);
        glBindVertexArray(0);
        glDepthFunc(GL_LESS); // set depth function back to default

        // glfw: swap buffers and poll IO events (keys pressed/released, mouse moved etc.)
        // -------------------------------------------------------------------------------
